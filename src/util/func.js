/**
 * Returns the sum of `n1` and `n2`
 * @param {number} n1
 * @param {number} n2
 */
export function sum(n1, n2) {
  return n1 + n2;
}

/**
 * Returns the product of `n1` and `n2`
 * @param {number} n1
 * @param {number} n2
 */
export function product(n1, n2) {
  return n1 * n2;
}

/**
 * Returns the difference between `n1` and `n2`
 * @param {number} n1
 * @param {number} n2
 */
export function diff(n1, n2) {
  return n1 - n2;
}

/**
 * Returns the quotient from dividing `n1` by `n2`
 * @param {number} n1
 * @param {number} n2
 */
export function quotient(n1, n2) {
  return n1 / n2;
}
